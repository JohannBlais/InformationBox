namespace InfoBox.Controls
{
    /// <summary>
    /// Represents which side borders are displayed.
    /// </summary>
    public enum SideBorder
    {
        /// <summary>
        /// No border
        /// </summary>
        None,
        /// <summary>
        /// Right side only
        /// </summary>
        Right,
        /// <summary>
        /// Left side only
        /// </summary>
        Left,
        /// <summary>
        /// Both sides (left and right)
        /// </summary>
        Both,
    }
}
